def __checkForNextPage(sHtmlContent):
    sPattern = 'class="next page-numbers" href="(.+?)"'
    oParser = cParser()
    aResult = oParser.parse(sHtmlContent, sPattern)
    if (aResult[0] == True):
        return aResult[1][0]

    return False

